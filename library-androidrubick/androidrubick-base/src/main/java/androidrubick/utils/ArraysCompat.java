    /**
     * 判断数组是否是空数组
     */
    public static boolean isEmpty(Object target) {
        if (isNull(target)) {
            return true;
        }
        if (target.getClass().isArray()) {
            return Array.getLength(target) == 0;
        }
        return false;
    }

